class Valid_Sudoku_1_Solution {
    public boolean isValidSudoku(char[][] board) {

        for(int i=0; i<9; i++){
            for(int j=0; j<9; j++){
                if(board[i][j] != '.'){

                    char c = board[i][j];

                    for(int row =0; row<9; row++){
                        if(board[row][j] == c && row != i){
                            return  false;
                        }
                    }

                    for(int col =0; col<9; col++){
                        if(board[i][col] == c && col != j){
                            return  false;
                        }
                    }

                    int row =  ( i / 3 ) * 3;
                    int rowLen = row + 3;

                    for( ; row < rowLen; row++){
                        int col = ( j / 3 ) * 3;
                        int colLen = col + 3;
                        for( ; col < colLen; col++){
                            //System.out.println(i + " "+j +" "+row + " "+col+" "+c);
                            if(board[row][col] == c && !( row == i && col == j )){
                                return  false;
                            }
                        }
                    }
                }
            }
        }

        return  true;
    }
}
